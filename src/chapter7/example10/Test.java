package chapter7.example10;
//Այս ծրագրում ցուցադրվում է public և private մոդիֆիկատորների տարբերությունը

public class Test {
    int a; //լռելայն սահմանվող  հասանելիություն
    public int b;//բաց հասանելիություն
    private int c; //փակ հասանելիություն

    // տվյալ կլասի c անդամի հասանելիության մեթոդներ
    void setc(int i) {
        //տվյալ կլասի c անդամի արժեքի սահմանում
        c = i;
    }

    int getc() {
        //վերցնել տվյալ կլասի c անդամի արժեքը
        return c;
    }
}
