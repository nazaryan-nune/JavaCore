package chapter7.example14Page205;
//Այս ծրագրում ցուցադրվում է զանգվածի երկարությունը որոշող length հատկության կիրառությունը

public class Length {
    public static void main(String[] args) {
        int[] a1 = new int[10];
        int[] a2 = {3, 5, 7, 1, 8, 99, 44, -10};
        int[] a3 = {4, 3, 2, 1};

        System.out.println("a1 զանգվածի երկարությունը` " + a1.length);
        System.out.println("a2 զանգվածի երկարությունը` " + a2.length);
        System.out.println("a3 զանգվածի երկարությունը` " + a3.length);
    }
}